    public static int ColorToTile(Color32 c)
    {
        string hex = RGBtoString(c);
        //print(hex);
        if (hex == null)
            return -1;
        if (!colorToTileMap.ContainsKey(hex))
            return -1;
        return (int)colorToTileMap[hex];
    }

